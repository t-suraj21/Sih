// Helper functions
export const getStatesByRegion = (region) => {
  return indianStates.filter(state => state.region === region);
};

export const getStateByName = (name) => {
  return indianStates.find(state => state.name === name);
};

export const getAllRegions = () => {
  return [...new Set(indianStates.map(state => state.region))];
};

export const getStatesByType = (type) => {
  return indianStates.filter(state => state.type === type);
};

